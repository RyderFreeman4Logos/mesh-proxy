use std::path::PathBuf;

use anyhow::Result;
use clap::{Parser, Subcommand, ValueEnum};
use mesh_proto::{MeshConfig, NodeRole};
use tracing_subscriber::EnvFilter;

#[derive(Parser)]
#[command(name = "mesh-proxy", about = "Decentralized P2P port forwarding and service discovery", version)]
struct Cli {
    #[command(subcommand)]
    command: Command,
}

#[derive(Subcommand)]
enum Command {
    /// Start the mesh-proxy daemon.
    Start {
        /// Node role: control or edge.
        #[arg(long)]
        role: Role,

        /// Path to config file.
        #[arg(long, default_value_os_t = MeshConfig::default_config_path())]
        config: PathBuf,
    },

    /// Stop the running daemon.
    Stop,

    /// Show daemon status.
    Status {
        /// Output format.
        #[arg(long, default_value_t = OutputFormat::Text)]
        output: OutputFormat,
    },

    /// Expose a local service to the mesh network.
    Expose,

    /// Accept an edge node's join ticket (control node only).
    Accept {
        /// The join ticket generated by the edge node.
        #[arg(long)]
        ticket: String,

        /// Optional friendly name for the edge node.
        #[arg(long)]
        name: Option<String>,
    },
}

/// Node role selection, maps to `NodeRole` from mesh-proto.
#[derive(Debug, Clone, Copy, ValueEnum)]
enum Role {
    Control,
    Edge,
}

impl From<Role> for NodeRole {
    fn from(role: Role) -> Self {
        match role {
            Role::Control => NodeRole::Control,
            Role::Edge => NodeRole::Edge,
        }
    }
}

/// Output format for the `status` subcommand.
#[derive(Debug, Clone, Copy, Default, ValueEnum)]
enum OutputFormat {
    #[default]
    Text,
    Json,
}

impl std::fmt::Display for OutputFormat {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Self::Text => write!(f, "text"),
            Self::Json => write!(f, "json"),
        }
    }
}

#[tokio::main]
async fn main() -> Result<()> {
    tracing_subscriber::fmt()
        .with_env_filter(EnvFilter::from_default_env())
        .init();

    let cli = Cli::parse();

    match cli.command {
        Command::Start { role, config } => {
            let node_role: NodeRole = role.into();
            println!(
                "Starting mesh-proxy daemon (role: {node_role:?})...\nConfig: {}",
                config.display()
            );
        }
        Command::Stop => {
            println!("Stopping mesh-proxy daemon...");
        }
        Command::Status { output } => {
            println!("Querying daemon status (format: {output})...");
        }
        Command::Expose => {
            println!("Not implemented in Phase 1");
        }
        Command::Accept { ticket, name } => {
            println!(
                "Accepting ticket: {ticket}{}",
                name.as_deref()
                    .map(|n| format!(" (name: {n})"))
                    .unwrap_or_default()
            );
            println!("Not implemented in Phase 1");
        }
    }

    Ok(())
}
